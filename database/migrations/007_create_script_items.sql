-- 007: アウト返し・Q&Aスクリプト管理テーブル
CREATE TABLE IF NOT EXISTS script_items (
  id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  type ENUM('rebuttal', 'qa') NOT NULL,
  category VARCHAR(100) DEFAULT NULL,
  industry VARCHAR(100) DEFAULT NULL,
  trigger_text VARCHAR(500) NOT NULL,
  response_text TEXT NOT NULL,
  status ENUM('approved', 'pending', 'rejected') NOT NULL DEFAULT 'approved',
  source_evaluation_id INT UNSIGNED DEFAULT NULL,
  source_call_id INT UNSIGNED DEFAULT NULL,
  sort_order INT NOT NULL DEFAULT 0,
  created_by INT UNSIGNED DEFAULT NULL,
  approved_by INT UNSIGNED DEFAULT NULL,
  approved_at DATETIME DEFAULT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_type_status (type, status),
  INDEX idx_category (category)
);

